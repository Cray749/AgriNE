// ─────────────────────────────────────────────────────────────────────────────
// Helper: Schedule timeline row with vertical dashed connector
// ─────────────────────────────────────────────────────────────────────────────
class _ScheduleTimelineRow extends StatelessWidget {
  final ScheduleItem item;
  final bool isLast;
  final int stepIndex;

  const _ScheduleTimelineRow({
    required this.item,
    required this.isLast,
    required this.stepIndex,
  });

  Color get _dotColor {
    switch (stepIndex) {
      case 0:
        return kGreenAccent;
      case 1:
        return kColorP;
      case 2:
        return kColorK;
      default:
        return kGreenAccent;
    }
  }

