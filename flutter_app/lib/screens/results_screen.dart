  late AnimationController _entryController;
  late Animation<double> _fadeAnim;
  late Animation<Offset> _slideAnim;

  @override
  void initState() {
    super.initState();
    _entryController = AnimationController(
      duration: const Duration(milliseconds: 600),
      vsync: this,
    );
    _fadeAnim = CurvedAnimation(
      parent: _entryController,
      curve: Curves.easeOut,
    );
    _slideAnim = Tween<Offset>(
      begin: const Offset(0, 0.05),
      end: Offset.zero,
    ).animate(CurvedAnimation(
      parent: _entryController,
      curve: Curves.easeOut,
    ));
    _entryController.forward();
  }

  @override
  void dispose() {
    _entryController.dispose();
    super.dispose();
  }

  // ── Share ──────────────────────────────────────────────────────────────────
  void _shareRecommendation(RecommendResponse data) {
    final StringBuffer sb = StringBuffer();
    sb.writeln('🌾 AgriSutra NE — Fertilizer Recommendation');
    sb.writeln('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
    sb.writeln('Crop: ${data.cropDisplay}');
    sb.writeln('Target Yield: ${data.targetYield} q/ha');
    sb.writeln('Land Size: ${data.landSizeAcres} acres');
    sb.writeln('');
    sb.writeln('📦 FERTILIZER DOSES (per hectare):');
    sb.writeln(
        '  • Urea  : ${data.nitrogen.productKgHa} kg/ha  (Total: ${data.nitrogen.productKgTotal} kg)');
    sb.writeln(
        '  • SSP   : ${data.phosphorus.productKgHa} kg/ha  (Total: ${data.phosphorus.productKgTotal} kg)');
    sb.writeln(
        '  • MOP   : ${data.potassium.productKgHa} kg/ha  (Total: ${data.potassium.productKgTotal} kg)');
    sb.writeln('');
    sb.writeln('📅 APPLICATION SCHEDULE:');
    for (final s in data.applicationSchedule) {
      sb.writeln('  Day ${s.daysAfterSowing} — ${s.timing}');
      sb.writeln('    ${s.description}');
    }
    sb.writeln('');
    sb.writeln('Generated by AgriSutra NE (STCR Method)');
    sb.writeln('ID: ${data.recommendationId}');

    Share.share(sb.toString(), subject: 'AgriSutra NE Fertilizer Recommendation');
  }

  // ── Formatted date ─────────────────────────────────────────────────────────
  String _formatDate(String isoString) {
    try {
      final dt = DateTime.parse(isoString).toLocal();
      const months = [
        '', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
        'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'
      ];
      return '${dt.day} ${months[dt.month]} ${dt.year}';
    } catch (_) {
      return isoString.substring(0, 10);
    }
  }

