  Future<void> _fetchAiSummary(RecommendResponse data) async {
    setState(() {
      _aiLoading = true;
      _aiError = null;
    });
    try {
      final summary = await AiService.getExplainableSummary(
        crop: data.cropDisplay,
        yieldTarget: data.targetYield,
        ureaKg: data.nitrogen.productKgTotal,
        sspKg: data.phosphorus.productKgTotal,
        mopKg: data.potassium.productKgTotal,
        weatherContext: data.weatherSummary?.advice,
      );
      if (mounted) setState(() => _aiSummary = summary);
